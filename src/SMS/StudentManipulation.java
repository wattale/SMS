private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
// TODO add your handling code here:
    if(fNameTF.getText().equalsIgnoreCase("")|homeADDTF.getText().equalsIgnoreCase("")|telephoneTF.getText().equalsIgnoreCase("")){
         JOptionPane.showMessageDialog(frame,"You are not allowed to leave any text field","Error",JOptionPane.ERROR_MESSAGE);
    }else{
        int n = JOptionPane.showConfirmDialog(frame, "Are you Sure? ", "Veriry", JOptionPane.YES_NO_OPTION);
        if(n==JOptionPane.YES_OPTION){                
                try {

                    Student modStdnt = new Student(indexTF.getText(), nameWithInTF.getText(), fNameTF.getText(), yearTF.getText() + "/" + monthTF.getText() + "/" + dateTF.getText(), (String) genderCB.getSelectedItem(), homeADDTF.getText(), telephoneTF.getText(),bufImgArray);
                    boolean upDated = clnt.upDate(modStdnt);
                    if (upDated) {
                        JOptionPane.showMessageDialog(frame, "SuccessFully Uploaded", "Message", JOptionPane.OK_OPTION);
                        editButton.setEnabled(true);
                        enableProfileMode(modStdnt);
                    } else {
                        JOptionPane.showMessageDialog(frame, "Error while upDating", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (IOException ex) {
                    Logger.getLogger(StudentManipulation.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(StudentManipulation.class.getName()).log(Level.SEVERE, null, ex);
                }
        }
        
    }
    
    
}//GEN-LAST:event_saveButtonActionPerformed

