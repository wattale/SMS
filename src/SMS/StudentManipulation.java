private void confirmButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmButtonActionPerformed
// TODO add your handling code here:
    if(passWordF.getText().equalsIgnoreCase("")|confirmPassWordF.getText().equalsIgnoreCase("")){
       JOptionPane.showMessageDialog(frame,"Fill all two fields before confirmation","Error",JOptionPane.ERROR_MESSAGE); 
    }else{
        if(passWordF.getText().equalsIgnoreCase(confirmPassWordF.getText())){
            int n = JOptionPane.showConfirmDialog(frame, "Are you Sure? ", "Veriry", JOptionPane.YES_NO_OPTION);
            if(n== JOptionPane.YES_OPTION){
                    try {
                        
                        boolean pWChanged = clnt.changePWord(indexTF.getText(), passWordF.getText());
                        if (pWChanged) {
                            JOptionPane.showMessageDialog(frame, "Password Changed successfully, click ok to continue", "Info", JOptionPane.OK_OPTION);
                            passWordF.setEditable(false);
                            confirmLB.setVisible(false);
                            confirmPassWordF.setText("");
                            confirmPassWordF.setVisible(false);
                            confirmButton.setVisible(false);
                            cancelPWchange.setVisible(false);
                            changeButton.setEnabled(true);
                        } else {
                            JOptionPane.showMessageDialog(frame, "Erro occured,Rechange password", "Error", JOptionPane.ERROR_MESSAGE);
                        }
                    } catch (IOException ex) {
                        Logger.getLogger(StudentManipulation.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (ClassNotFoundException ex) {
                        Logger.getLogger(StudentManipulation.class.getName()).log(Level.SEVERE, null, ex);
                    }
            }
            
        }else{
            JOptionPane.showMessageDialog(frame,"Enter the correct password","confirmation Erro",JOptionPane.ERROR_MESSAGE);
        }
    }
    
}//GEN-LAST:event_confirmButtonActionPerformed

private void changePhotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changePhotoActionPerformed
    // TODO add your handling code here:
      final JFileChooser fc = new JFileChooser();
      fc.setAcceptAllFileFilterUsed(false);
      fc.setFileFilter(new SMS.ImageFilter());

    int status = fc.showOpenDialog(this);

    try {
        if (status == JFileChooser.APPROVE_OPTION) {
            bufImg = ImageIO.read(fc.getSelectedFile());
            this.setImage(this.imageToBytes(bufImg));
            bufImgArray = imageToBytes(bufImg);
        }

    } catch (IOException e) {
        e.printStackTrace();
   }
}//GEN-LAST:event_changePhotoActionPerformed





 public byte[] imageToBytes(BufferedImage img){
        byte[] imageInByte = null;
        ByteArrayOutputStream baos = new ByteArrayOutputStream();
        try{
	ImageIO.write( img, "jpg", baos );
	baos.flush();
	imageInByte = baos.toByteArray();
	baos.close();}
        catch(Exception e){

        }
        return imageInByte;
    }

